public class Studet
{
	Studet(int high, int age)
	{
		this.high = high;
		this.age = age;
	}
	private int high;
	private int age;

	public int getHigh()
	{
		return high;
	}

	public void setHigh(int high)
	{
		this.high = high;
	}

	public int getAge()
	{
		return age;
	}

	public void setAge(int age)
	{
		this.age = age;
	}
}
